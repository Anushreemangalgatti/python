#swapcase of string

Name_1 ="anushree"
Name_2 ="ANUSHREE"
Name_3 ="AnUsHrEe"

#converts string into uppercase
print(Name_1.swapcase())

#converts string intolowercase
print(Name_2.swapcase())

#converts string uppercaseto lowercase and vise versa
print(Name_3.swapcase())


#output
#ANUSHREE
#anushree
#aNuShReE
